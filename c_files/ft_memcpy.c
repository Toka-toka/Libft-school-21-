#include <stdio.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char *dest_point;
	char *src_point;

	dest_point = (char*)dest;
	src_point = (char*)src;
	while (n > 0)
	{
		*dest_point++ = *src_point++;
		n--;
	}
	return (dest);
}
